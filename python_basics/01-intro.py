# What is python?

# :python is a High-Level,Interpreted,general-purpose programming Language created by Guido van Rossum in 1991.
# It is one of the most popular and beginner-friendly programming languages in the world because of its simple syntax and powerful features.

# Think of Python as a translator between human language and computer language.
# You write instructions in Python → Python translates them → The computer executes them.